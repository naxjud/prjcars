    public function executeSearch(Request $request){
        $keywords = $request->input('keywords');

        $cars = Car::all();

        $searchCars = new Collection();
        
        foreach ($cars as $car ) {
            if(Str::contains(Str::lower($car->make), Str::lower($keywords)))
                $searchCars->add($car);
        }

        return view('cars/result', compact('searchCars'));

    }

